 //Get our library for uploading. We are a thin wrapper over this
require_once($CFG->dirroot . '/repository/upload/lib.php');
 
class repository_poodllplus extends repository_upload {

 

       /**
     * Return a upload form
     * @return array
     */
    public function get_listing() {
        global $CFG;
        $ret = array();
        $ret['nologin']  = true;
        $ret['nosearch'] = true;
        $ret['norefresh'] = true;
        $ret['list'] = array();
        $ret['dynload'] = false;
        $ret['upload'] = array('label'=>get_string('attachment', 'repository'), 'id'=>'repo-form');
        return $ret;
    }
}
